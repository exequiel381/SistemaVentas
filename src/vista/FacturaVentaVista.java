    public void LlenarFactura(int numeroFactura,Venta venta){
        
        lbFactura.setText(""+numeroFactura);
        lbTotal.setText(""+venta.getTotalConIVA());
        lbDescuento.setText(""+venta.getDescuento());
        lbFecha.setText(venta.getFecha());
        lbSubTotal.setText(venta.getTotal()+"");
        lbIVA.setText(venta.getMontoIVA()+"");
        
        DefaultTableModel detalle = new DefaultTableModel();
        detalle.addColumn("idLineaDeVenta");
        detalle.addColumn("Producto");
        detalle.addColumn("Descripcion");
        detalle.addColumn("Precio Unitario");
        detalle.addColumn("Cantidad");
        detalle.addColumn("Subtotal");
         
        tablaLineas.setModel(detalle);
         
         
        //detalle.setRowCount(0);
         
         for (LineaDeVenta l : venta.getLineasVenta()) {
           detalle.addRow(new Object[]{""+l.getIdLineaVenta(),l.getProducto().getIdProducto(),l.getProducto().getDescripcion(),""+l.getProducto().getPrecio(),""+l.getCantidad(),""+l.getSubTotal()});
         }
         
         this.setVisible(true);
    }

